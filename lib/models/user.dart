class User {
  String id;
  String firstName;
  String lastName;
  String email;
  String userImage;
  String address;
  String userBio;
  User({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.userImage,
    required this.address,
    required this.userBio,
  });
}
